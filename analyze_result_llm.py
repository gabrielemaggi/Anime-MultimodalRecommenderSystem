import json
import logging
from collections import defaultdict
from datetime import datetime
from pathlib import Path

import ollama

# ============================================================================
# CONFIGURATION
# ============================================================================

INPUT_DIR = Path("./evaluation_results")

# This model will read the comments and summarize them for you.
# Use a smart model with a large context window if possible.
ANALYSIS_MODEL = "qwen2.5:14b"

OLLAMA_HOST = "http://192.168.99.95:11434"

# ============================================================================
# SETUP
# ============================================================================
logging.basicConfig(level=logging.INFO, format="%(message)s")
logger = logging.getLogger("RecommenderAnalyst")


def load_latest_results():
    files = list(INPUT_DIR.glob("detailed_results_*.json"))
    if not files:
        print("❌ No results found.")
        return None
    latest = max(files, key=lambda p: p.stat().st_mtime)
    print(f"📂 Loading data from: {latest.name}")
    with open(latest, "r", encoding="utf-8") as f:
        return json.load(f)


def aggregate_feedback(data):
    """
    Pools all comments together to find global patterns in the Recommender's behavior.
    """
    feedback = {
        "success_stories": [],  # Score 4-5
        "failure_patterns": [],  # Score 1-2
        "genre_issues": [],  # Specific comments on Genre Goals
    }

    print(f"🔍 Processing {len(data)} evaluations...")

    for entry in data:
        eval_data = entry.get("evaluation", {})
        if not eval_data or "error" in entry:
            continue

        score = eval_data.get("score", 0)
        reasoning = eval_data.get("reasoning", "No reasoning provided.")
        genre_reasoning = eval_data.get("genre_goal_reasoning", "")

        # We include the User and Goal in the text so the LLM has context
        context_str = f"[User: {entry['username']} | Goal: {entry['genre_goal']}] -> Comment: {reasoning}"

        if score >= 4:
            feedback["success_stories"].append(context_str)
        elif score <= 2:
            feedback["failure_patterns"].append(context_str)

        if entry.get("genre_goal") and genre_reasoning:
            feedback["genre_issues"].append(
                f"[Goal: {entry['genre_goal']}] -> {genre_reasoning}"
            )

    return feedback


def generate_strategic_report(feedback_data):
    """
    Asks Ollama to act as a Product Manager and analyze the system's strengths/weaknesses.
    """
    print(f"🧠 Generating analysis using {ANALYSIS_MODEL}...")

    # We take a sample to fit in context window, prioritizing distinct feedback
    # (Taking last 30 entries of each to ensure we get recent data)
    success_txt = "\n".join(feedback_data["success_stories"][-40:])
    fail_txt = "\n".join(feedback_data["failure_patterns"][-40:])
    genre_txt = "\n".join(feedback_data["genre_issues"][-40:])

    prompt = f"""
    You are the Lead Data Scientist analyzing the performance of a new Anime Recommender System.
    Below are logs of automated reviews (scores and comments) for the recommendations generated by the system.

    YOUR GOAL: Identify patterns in the Recommender's logic. What is it doing right? What is it breaking?

    === DATA: SUCCESSFUL RECOMMENDATIONS (High Scores) ===
    {success_txt}

    === DATA: FAILED RECOMMENDATIONS (Low Scores) ===
    {fail_txt}

    === DATA: GENRE FILTERING PERFORMANCE ===
    {genre_txt}

    === REQUIRED REPORT FORMAT (Markdown) ===

    # 🚀 Recommender System Performance Report

    ## 1. 🌟 Highlights (Punti di Forza)
    *Analyze the 'Success' data. What connections is the system making correctly?*
    * **Visual/Studio Matching:** (Does it correctly group specific studios?)
    * **Thematic Consistency:** (Does it capture mood/tone well?)
    * **Narrative Continuity:** (Does it find relevant sequels/prequels?)

    ## 2. ⚠️ Critical Flaws (Difetti Principali)
    *Analyze the 'Failure' data. Why are users/evaluators rejecting the suggestions?*
    * **Genre Hallucinations:** (Does it recommend Action when asked for Romance?)
    * **Tonal Dissonance:** (Does it recommend dark horror to a slice-of-life user?)
    * **Repetition:** (Are there complaints about lack of variety?)

    ## 3. 🎯 Genre Goal Analysis
    *How strictly does the system obey the explicit 'Genre Goal' filter?*
    * Is it too loose (ignoring the goal)?
    * Is it too strict (ignoring quality for the sake of the keyword)?

    ## 4. 🛠️ Actionable Recommendations
    *Give 3 specific technical suggestions to improve the vector search or filtering logic based on these errors.*
    """

    client = ollama.Client(host=OLLAMA_HOST)

    try:
        response = client.chat(
            model=ANALYSIS_MODEL,
            messages=[{"role": "user", "content": prompt}],
            options={"temperature": 0.5},  # Lower temp for more analytical output
        )
        return response["message"]["content"]
    except Exception as e:
        return f"Error during analysis: {e}"


# ============================================================================
# MAIN
# ============================================================================

if __name__ == "__main__":
    data = load_latest_results()
    if data:
        # 1. Aggregate
        feedback = aggregate_feedback(data)

        # 2. Analyze
        report = generate_strategic_report(feedback)

        # 3. Save
        filename = f"recommender_analysis_{datetime.now().strftime('%Y%m%d_%H%M%S')}.md"
        with open(INPUT_DIR / filename, "w", encoding="utf-8") as f:
            f.write(report)

        print(f"\n✅ Analysis Complete! Report saved to: {INPUT_DIR / filename}")
        print("\nPreview of Actionable Recommendations:")
        print("-" * 50)
        # Simple trick to print just the end of the report to console
        print(
            report.split("## 4.")[-1] if "## 4." in report else "See file for details."
        )
